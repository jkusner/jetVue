function setHeatmapGradient(heatmap) {
  const gradient = [
    'rgba(0, 64, 255, 0)',
    'rgba(0, 64, 255, 1)',
    'rgba(0, 102, 255, 1)',
    'rgba(0, 128, 255, 1)',
    'rgba(0, 191, 255, 1)',
    'rgba(0, 255, 255, 1)'   
  ]
  heatmap.set('gradient', gradient)
  heatmap.set('radius', 15)
}

const dashedLineSymbol = {
  path: 'M 0,-5 0,5',
  strokeOpacity: .85,
  scale: 1
};

function routeToStyledPath({ap1, ap2, ap3, isMint}) {
  let points = []
  points.push(jetBlueData.airports[ap1].Location)
  if (ap2) {
    points.push(jetBlueData.airports[ap2].Location)
  }
  points.push(jetBlueData.airports[ap3].Location)
  return new google.maps.Polyline({
    path: points,
    geodesic: true,
    strokeColor: isMint ? '#69c040' : '#cce0ff',
    strokeOpacity: 0,
    // strokeColor: isMint ? '#0000FF' : '#222222',
    // strokeOpacity: isDirect ? 1.0 : 0,
    icons: [{
      icon: dashedLineSymbol,
      offset: '0',
      repeat: '20px'
    }],
    strokeWeight: 1
  })
}

